def comb(idx, selected):
    global max_v

    if idx == N:
        subset = []
        for i in selected:
            subset.append(time[i])
        
        cnt = 1
        for j in range(0, len(subset)-1):
            if subset[j][1] <= subset[j+1][0]:
                cnt = cnt + 1

        if max_v < cnt:
            max_v = cnt
            
        return

    selected.append(idx)
    comb(idx+1, selected)

    selected.pop()
    comb(idx+1, selected)



T = int(input())
for tc in range(1, T + 1):
    max_v = 0
    N = int(input())
    time = []
    for i in range(0, N):
        s, e = map(int, input().split())  
        time.append([s, e])
    # time.sort(key= lambda x:x[0])
    time.sort(key=lambda x:x[1])
    comb(0, [])
    print(f"#{tc} {max_v}")

  

