def 부분집합_구하기(집합):
    n = len(집합)  # 원래 집합의 원소 개수를 가져옵니다.
    부분집합들 = []  # 모든 부분집합을 저장할 리스트를 생성합니다.

    for i in range(2 ** n):  # 0부터 2^n - 1까지의 숫자를 반복합니다. 이 숫자들은 부분집합을 나타냅니다.
        부분집합 = []  # 현재 숫자에 해당하는 부분집합을 저장할 리스트를 생성합니다.
        for j in range(n):
            # i의 j번째 비트가 1인 경우에만 해당 원소를 부분집합에 추가합니다.
            if i & (1 << j):
                부분집합.append(집합[j])
        부분집합들.append(부분집합)  # 현재 부분집합을 전체 부분집합 리스트에 추가합니다.

    return 부분집합들

원래집합 = [1, 2, 3]
부분집합들 = 부분집합_구하기(원래집합)
print(부분집합들)